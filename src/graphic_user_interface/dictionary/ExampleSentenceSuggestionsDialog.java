    private void showASuggestions() {
        List<String> exampleSentenceSuggestions
                = exampleSentenceAssigner.getSuggestions(card.index);

        for (int i = 0; i < exampleSentenceSuggestions.size(); i++) {
                tableModel.addRow(new Object[]{
                    exampleSentenceSuggestions.get(i)});
        }
    }

